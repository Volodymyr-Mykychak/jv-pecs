package core.mate.academy.model;
/**
 * Інтерфейс `Workable` визначає метод `doWork()`, який представляє робочу дію.
 * Класи, що реалізують цей інтерфейс, повинні надати власну реалізацію цього методу.
 */
public interface Workable {

    /**
     * Виконує робочу дію.
     */
    void doWork();
}
